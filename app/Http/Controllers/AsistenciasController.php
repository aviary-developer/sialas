    {
      $var=0;
        $id=User::where('codigo',$request['users_id'])->get();
        foreach ($id as $i) {
          $var=$i->id;
        }
        Asistencia::create([
          'users_id'=>$var,
        ]);
        Bitacoras::bitacora("Registro de asistencia");
